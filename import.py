import math
p = math.pow(4,3)       # returns the result of the first number raised 
                        # to the power of second.
print(p)
a = math.floor(4.3)
b = math.floor(10.9)
print(a,b)

a = math.ceil(4.3)
b = math.ceil(10.9)
print(a,b)

s = abs(10)         # not a function of math module
t = abs(-10)
print(s, t)

l = math.log(10)
m = math.log(10,2)  # returns the logarithm of the first number to 
                    # the base of the second number
print(l) 
print(m)            

sq = math.sqrt(4)
print(sq)

